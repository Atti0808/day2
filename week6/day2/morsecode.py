def text_to_morse_code(text):
    text = text.upper()
    morse_code = ""

    for char in text:
        if char in text:
            morse_code += text[char]
        elif char == " ":
            morse_code += "  "
        else:
            morse_code += "?"
   
    return morse_code

def main():
    text = input ("Enter the text to conver to Morse code: ")

    morse_code = text_to_morse_code(text) 
    print("Morse Code", morse_code) 

